#include "push_swap.h"

int	ft_is_sorted(int *tab, int size)
{
	int	i;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
			i++;
		else
			return (1);
	}
	return (0);
}
